//examples of a while loop
/*while loop will keep looping until the condition returns true
(not self contained)
*/
var i =3
while (i <4){
    console.log(i);
    i =i+1;
}
/*the value set before the loop and clearly defined
specify the exit condition 
in the parentheses 
of the loop declaration
counter example:
*/ 
var counter =3

while (counter >0) {
    console.log(counter);
    counter = counter -1;
}
